def getExitTiles(roomId, screenW, screenH):
    layout   = rooms[roomId].layout
    rowCount = len(layout)
    colCount = len(layout[0])
    blockW   = screenW / colCount
    blockH   = screenH / rowCount
    exitRects = []
    for rowIdx, rowData in enumerate(layout):
        for colIdx, tileVal in enumerate(rowData):
            if tileVal == 3:
                exitRects.append(pygame.Rect(colIdx * blockW, rowIdx * blockH, blockW, blockH))
    return exitRects

def getWallRects(roomId, screenW, screenH):
    layout   = rooms[roomId].layout
